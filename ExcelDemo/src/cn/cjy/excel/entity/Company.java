package cn.cjy.excel.entity;

public class Company {
	public Integer id;
	public String name;
	
	public Company() {
		super();
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
